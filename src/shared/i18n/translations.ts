export type TranslationKey = keyof typeof dictionaries.it;

export function translate(language: Language, key: TranslationKey, params?: TranslationParams): string {
  let text: string = dictionaries[language][key] ?? dictionaries.it[key];

  if (!params) {
    return text;
  }

  for (const [name, value] of Object.entries(params)) {
    text = text.replaceAll(`{{${name}}}`, String(value));
  }
  return text;
}

export function isLanguage(value: unknown): value is Language {
  return value === 'it' || value === 'en';
}

export function getMealTypeTranslationKey(mealType: MealType): TranslationKey {
  const keys: Record<MealType, TranslationKey> = {
    breakfast: 'mealBreakfast',
    lunch: 'mealLunch',
    dinner: 'mealDinner',
  };
  return keys[mealType];
}
